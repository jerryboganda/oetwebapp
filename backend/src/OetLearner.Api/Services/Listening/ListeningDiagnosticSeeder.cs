    // ────────────────────────── helpers ────────────────────────────────────

    private static string QuestionId(string prefix, int n) => $"{prefix}-Q{n}";

    private async Task<ListeningPart> UpsertPartAsync(
        string paperId,
        string partId,
        ListeningPartCode partCode,
        int maxRawScore,
        string instructions,
        DateTimeOffset now,
        CancellationToken ct)
    {
        var existing = await db.ListeningParts.FirstOrDefaultAsync(p => p.Id == partId, ct);
        if (existing is not null) return existing;

        var part = new ListeningPart
        {
            Id = partId,
            PaperId = paperId,
            PartCode = partCode,
            MaxRawScore = maxRawScore,
            Instructions = instructions,
            CreatedAt = now,
            UpdatedAt = now,
        };
        db.ListeningParts.Add(part);
        return part;
    }

    private async Task<ListeningExtract> UpsertExtractAsync(
        string id,
        string partId,
        int displayOrder,
        ListeningExtractKind kind,
        string title,
        string? accentCode,
        string speakersJson,
        string transcriptSegmentsJson,
        string? topicCsv,
        DateTimeOffset now,
        CancellationToken ct)
    {
        var existing = await db.ListeningExtracts.FirstOrDefaultAsync(e => e.Id == id, ct);
        if (existing is not null) return existing;

        var extract = new ListeningExtract
        {
            Id = id,
            ListeningPartId = partId,
            DisplayOrder = displayOrder,
            Kind = kind,
            Title = title,
            AccentCode = accentCode,
            SpeakersJson = speakersJson,
            AudioStartMs = null,
