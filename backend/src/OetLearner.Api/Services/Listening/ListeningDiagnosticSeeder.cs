    private void AddMcq(
        string paperId,
        string partId,
        string extractId,
        string id,
        int questionNumber,
        int displayOrder,
        string stem,
        (string Key, string Text, bool Correct, ListeningDistractorCategory? Cat)[] options,
        string correctKey,
        string subSkills,
        string accent,
        DateTimeOffset now)
    {
        var q = new ListeningQuestion
        {
            Id = id,
            PaperId = paperId,
            ListeningPartId = partId,
            ListeningExtractId = extractId,
            QuestionNumber = questionNumber,
            DisplayOrder = displayOrder,
            Points = 1,
            QuestionType = ListeningQuestionType.MultipleChoice3,
            Stem = stem,
            CorrectAnswerJson = System.Text.Json.JsonSerializer.Serialize(correctKey),
            AcceptedSynonymsJson = null,
            CaseSensitive = false,
            ExplanationMarkdown = null,
            SkillTag = null,
            SubSkillTagsCsv = subSkills,
            Accent = accent,
            DifficultyLevel = 3,
            Version = 1,
            CreatedAt = now,
            UpdatedAt = now,
        };
        db.ListeningQuestions.Add(q);

        var order = 0;
        foreach (var (key, text, correct, cat) in options)
        {
            order++;
            db.ListeningQuestionOptions.Add(new ListeningQuestionOption
            {
                Id = $"{id}-opt-{key}",
                ListeningQuestionId = id,
                OptionKey = key,
                DisplayOrder = order,
                Text = text,
                IsCorrect = correct,
                DistractorCategory = correct ? null : cat,
                WhyWrongMarkdown = null,
                Version = 1,
            });
        }
    }

