tail -3 /tmp/generate-pronunciation-live.log 2>/dev/null || echo "(no log)"
echo ""
echo "=== VOCAB ==="
tail -3 /tmp/vocab-live.log 2>/dev/null || echo "(no log)"
echo ""
echo "=== BACKEND ERRORS (last 5m, non-access) ==="
docker logs --since 5m oet-api 2>&1 | grep -vE 'HTTP/1.1" [0-9]{3}' | grep -iE 'exception|error|warn|fail|stacktrace|aigateway' | tail -30 || echo "(none)"
