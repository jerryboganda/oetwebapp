export function resolveWebsitePackageBySlug(slug: string): WebsitePackage | undefined {
  const s = slug.trim().toLowerCase();
  return WEBSITE_PACKAGES.find((p) => p.slug === s);
}

export function resolveWebsitePackageByCode(code: string): WebsitePackage | undefined {
  return WEBSITE_PACKAGES.find((p) => p.code === code);
}

/** Apply an admin-edited overlay to a static website package without mutating the base. */
export function applyWebsitePackageOverlay(
  pkg: WebsitePackage,
  overlay?: import('./catalog-presentation').WebsitePackageOverlay | null,
): WebsitePackage {
  if (!overlay) return pkg;
  return {
    ...pkg,
    name: overlay.name ?? pkg.name,
    metaChips: overlay.metaChips ?? pkg.metaChips,
    formatLine: overlay.formatLine ?? pkg.formatLine,
    description: overlay.description ?? pkg.description,
    badges: overlay.badges ?? pkg.badges,
    features: overlay.features ?? pkg.features,
    bestFor: overlay.bestFor ?? pkg.bestFor,
    featured: overlay.featured ?? pkg.featured,
    packageNo: overlay.packageNo ?? pkg.packageNo,
  };
}
