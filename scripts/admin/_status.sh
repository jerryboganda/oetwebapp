echo "=== READING ==="
tail -6 /tmp/generate-reading-live.log 2>/dev/null || echo "(no log)"
echo ""
echo "=== GRAMMAR ==="
tail -3 /tmp/generate-grammar-live.log 2>/dev/null || echo "(no log)"
echo ""
