// ═════════════════════════════════════════════════════════════════════════════
// ListeningDiagnosticSeeder — Stage 2 of OET_LISTENING_MODULE_PATHWAY.md §6.1.
//
// Idempotently materialises the Phase 1 23-item diagnostic mini-test into the
// relational Listening tables (ContentPaper / ListeningPart / ListeningExtract
// / ListeningQuestion / ListeningQuestionOption). The structure is:
//
//   • Part A mini   — 1 consultation extract  → 6 short-answer gap-fills
//                      (British male doctor + Australian female patient)
//                      Sub-skills: L1, L2, L3, L8
//   • Part B mini   — 3 workplace extracts    → 1 MCQ each (3 questions total)
//                      Accents: en-GB, en-AU, en-US (one each)
//                      Sub-skills: L4, L5, L8
//   • Part C mini   — 1 presentation extract  → 6 MCQ
//                      (Australian male professional, stroke rehabilitation)
//                      Sub-skills: L5, L6, L7, L8
//   • Accent test   — 4 short clips           → 1 MCQ each (4 questions total)
//                      Accents: en-GB, en-AU, en-US, en-XX (non-native)
//                      Sub-skills: L8 (accent-specific)
//
//   Total = 6 + 3 + 6 + 4 = 23 items.
//
// Phase 1 audio note:
//   The diagnostic audio is NOT generated by the TTS pipeline yet — extracts
//   are seeded with realistic transcript text but `AudioContentSha = null`.
//   The diagnostic UI must gracefully degrade ("Audio coming soon" + skip-to-
//   question) when AudioContentSha is null. A later phase will wire the TTS
//   worker to synthesise these clips and back-fill the SHAs.
//
// Idempotency:
//   • String slugs for ListeningPart and ListeningQuestion → re-runs in dev
//     are pure no-ops.
//   • Deterministic GUID strings for ListeningExtract.Id (derived from a
//     fixed namespace so the value is stable across machines).
//   • Gated by ListeningDiagnosticSeederOptions.Enabled (default false).
//
// Configuration:
//   Seed:ListeningDiagnostic:Enabled = true   // opt-in
//   Seed:ListeningDiagnostic:PaperId          // override the default PaperId
// ═════════════════════════════════════════════════════════════════════════════

public sealed class ListeningDiagnosticSeederOptions
{
    public const string SectionName = "Seed:ListeningDiagnostic";

    /// <summary>If false, the seeder no-ops (default). Opt in by setting
    /// <c>Seed:ListeningDiagnostic:Enabled = true</c> in appsettings or
    /// via the <c>SEED__LISTENINGDIAGNOSTIC__ENABLED</c> env var.</summary>
    public bool Enabled { get; set; } = false;

    /// <summary>ContentPaper.Id (and slug) used to anchor the diagnostic.
    /// Override if your environment already has a paper with this id.</summary>
    public string PaperId { get; set; } = "listening-diagnostic-phase1";
}

